export const getLighterOrDarkerColor = (color: string, percentage: number) => {
	const hex = color.replace("#", "");
	const r = parseInt(hex.substring(0, 2), 16);
	const g = parseInt(hex.substring(2, 4), 16);
	const b = parseInt(hex.substring(4, 6), 16);

	const newR = Math.round(r + (255 - r) * percentage);
	const newG = Math.round(g + (255 - g) * percentage);
	const newB = Math.round(b + (255 - b) * percentage);

	return `#${newR.toString(16).padStart(2, "0")}${newG
		.toString(16)
		.padStart(2, "0")}${newB.toString(16).padStart(2, "0")}`;
};

export const getMutedColor = (color: string) => {
	if (color.startsWith("#000")) {
		return "#555555";
	} else if (color.startsWith("#fff")) {
		return "#888888";
	}
};

export const getContrastingColor = (color: string) => {
	// color is passed in as a hex string

	const r = parseInt(color.slice(1, 3), 16);
	const g = parseInt(color.slice(3, 5), 16);
	const b = parseInt(color.slice(5, 7), 16);

	const yiq = (r * 299 + g * 587 + b * 114) / 1000;

	return yiq >= 128 ? "#000000" : "#ffffff";
};
